# Polimorfismo, como o próprio nome diz "poli",quer dizer várias,no caso
#da programação quer dizer que uma mesma função método ou operador pode
# ter mais de uma função.Um exemplo clássico é o operador de soma(+) que
#quando colocado com números ele soma como no,exemplo1 abaixo q ue logo farei
#agora esse mesmo operador quando colocado com "str" ,ele faz contenação,
#outro exemplo também com operadore é o (*) que quando colocado somente uma
# vez ele faz o papel de fazer a multiplicação,agr quando colocado duas vezes(**)
#ele faz o palpel de exponenciação como no exemplo2 que farei abaixo.

